# Defining a empty bag (z)
z=[]
print(z)
# Empty() = O(1)
emp=len(z)
print(emp)
# Add(a) = O(1)
z.append(1)
z.append(5)
z.append('s')
z.append(False)
print(z)
# Size() = O(1)
s=len(z)
print(s)

# Defining a empty stack (x)
x=[]
print(x)
# Empty() = O(1)
emp=len(x)
print(emp)
# Push(a) = O(1)
x.append(1)
x.append(5)
x.append('s')
x.append(False)
print(x)
# Size() = O(1)
s=len(x)
print(s)
# Pop() = O(1)
print(x.pop())
print(x.pop())
print(x)

# Queue
# Defining a empty queue (y)
y=[]
print(y)
# Enqueue(a) = O(1)
y.insert(0,1)
y.insert(0,5)
y.insert(0,'s')
y.insert(0,False)
print(y)
# Front() = O(1)
f=y[0]
print(f)
# Rear() = O(1)
r=y[len(y)-1]
print(r)
# Dequeue() = O(1)
print(y.pop())
print(y.pop())
print(y)
